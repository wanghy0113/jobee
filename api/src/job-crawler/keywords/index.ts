type JobType =
  | 'techJob'
  | 'designJob'
  | 'hrJob'
  | 'educationJob'
  | 'healthcareJob'
  | 'financeJob'
  | 'marketingJob'
  | 'legalJob'
  | 'salesJob'
  | 'operationsJob'
  | 'customerServiceJob'
  | 'creativeJob'

function getJobType(jobTitle: string): JobType[] {
  const title = jobTitle.toLowerCase()
  const jobTypes: JobType[] = []

  if (
    title.includes('developer') ||
    title.includes('engineer') ||
    title.includes('data') ||
    title.includes('programmer') ||
    title.includes('software')
  ) {
    jobTypes.push('techJob')
  }
  if (
    title.includes('design') ||
    title.includes('ui') ||
    title.includes('ux') ||
    title.includes('graphic')
  ) {
    jobTypes.push('designJob')
  }

  if (
    title.includes('hr') ||
    title.includes('human resources') ||
    title.includes('recruiter')
  ) {
    jobTypes.push('hrJob')
  }

  if (
    title.includes('teacher') ||
    title.includes('professor') ||
    title.includes('instructor') ||
    title.includes('educator')
  ) {
    jobTypes.push('educationJob')
  }

  if (
    title.includes('nurse') ||
    title.includes('doctor') ||
    title.includes('physician') ||
    title.includes('healthcare') ||
    title.includes('medical')
  ) {
    jobTypes.push('healthcareJob')
  }

  if (
    title.includes('accountant') ||
    title.includes('finance') ||
    title.includes('financial') ||
    title.includes('analyst')
  ) {
    jobTypes.push('financeJob')
  }

  if (
    title.includes('marketing') ||
    title.includes('brand') ||
    title.includes('seo') ||
    title.includes('content')
  ) {
    jobTypes.push('marketingJob')
  }

  if (
    title.includes('attorney') ||
    title.includes('lawyer') ||
    title.includes('legal') ||
    title.includes('paralegal')
  ) {
    jobTypes.push('legalJob')
  }

  if (
    title.includes('sales') ||
    title.includes('account manager') ||
    title.includes('business development')
  ) {
    jobTypes.push('salesJob')
  }

  if (
    title.includes('operations') ||
    title.includes('logistics') ||
    title.includes('supply chain')
  ) {
    jobTypes.push('operationsJob')
  }

  if (
    title.includes('customer service') ||
    title.includes('support') ||
    title.includes('client services')
  ) {
    jobTypes.push('customerServiceJob')
  }

  if (
    title.includes('design') ||
    title.includes('art') ||
    title.includes('creative') ||
    title.includes('writer')
  ) {
    jobTypes.push('creativeJob')
  }

  return jobTypes
}

