export function findKeywordsForJob(jobTitle: string, jobDescription: string) {
  const jobTypes = getJobType(jobTitle)
  jobDescription = jobDescription.toLowerCase()
  const foundKeywords = new Set<string>()

  for (const jobType of jobTypes) {
    const keywords = keywordsMap[jobType]
    for (const { keyword, matchingStrings } of keywords) {
      for (const string of matchingStrings) {
        if (jobDescription.includes(string)) {
          foundKeywords.add(keyword)
          break
        }
      }
    }
  }

  return Array.from(foundKeywords)
}
